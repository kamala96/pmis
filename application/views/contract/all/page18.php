<p style="text-align:right;"><strong> Annexure II</strong></p>
<p><strong> Description of Key Terms</strong></p>
<table style="width:100% !important;" border="1">
<tbody>
<tr>
<td width="25%">
<p>Operating Revenue</p>
</td>
<td colspan="2" width="74%">
<p>Revenues generated from a company's core/primary business activities.</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Operating Expenses</p>
</td>
<td colspan="2" width="74%">
<p>Costs incurred from normal operating activities/business operations.</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Net Profit before tax</p>
</td>
<td colspan="2" width="74%">
<p>Operating Profit after deducting operating expenses and other charges (include interest) before income tax.</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Administration Costs</p>
</td>
<td colspan="2" width="74%">
<p>Sum of all necessary costs to maintain daily operations and administer the business (incl. salaries and general services), BUT excluding direct costs attributable to the production of goods and services.</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Employee Costs</p>
</td>
<td colspan="2" width="74%">
<p>The sum of employee basic salaries, taxes and benefits</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Extra ordinary items</p>
</td>
<td colspan="2" width="74%">
<p>Special transactions/items with significant impact on profitability and/or sustainability, or unusual and/or infrequent in nature, and/or which do not relate to the core business objectives.</p>
<p>(E.g., gains or losses from fair valuation, or from disposal of assets).</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Average Assets</p>
</td>
<td colspan="2" width="74%">
<p>Average of the total assets by adding total asset at the beginning of the accounting period to the value of total asset at the end of the accounting period dividing by two.</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Average Equity</p>
</td>
<td colspan="2" width="74%">
<p>Adding the shareholders' equity at the beginning of a period to the shareholders' equity at period's end and dividing by two.</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Shareholder’s Equity</p>
</td>
<td colspan="2" width="74%">
<p>The amount of capital contributed by the owners to the business, and it includes capital funds, reserves, accumulated surplus and retained earnings</p>
<p>(OR the difference between a company's total assets and its total liabilities)</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Debt</p>
</td>
<td colspan="2" width="74%">
<p>Connotes total liability including current and non-current liabilities representing all amounts the business owes to other people.</p>
</td>
</tr>
<tr>
<td width="25%">
<p>Financial Pay-out</p>
</td>
<td width="74%">
<p>The amount of money remitted by a PSC to the shareholder/Government either as dividends from profits or from revenues/collections mandated by the government.</p>
</td>
<td width="0%">
<p> </p>
</td>
</tr>
<tr>
<td width="25%">
<p>Dividend</p>
</td>
<td width="74%">
<p>Payment made by a PSC to the shareholders as a distribution of profits.</p>
</td>
<td width="0%">
<p> </p>
</td>
</tr>
<tr>
<td width="25%">
<p>Retained Collections</p>
</td>
<td width="74%">
<p>Amount of money collected by a non-commercial PSC and not remitted to the Government</p>
</td>
<td width="0%">
<p> </p>
</td>
</tr>
<tr>
<td width="25%">
<p>Government Subvention</p>
</td>
<td width="74%">
<p>Funds granted by the Government from its budget to a PSC to support operations (in specific pre-determined areas)</p>
</td>
<td width="0%">
<p> </p>
</td>
</tr>
</tbody>
</table>
